func testpacketOSPFHello(testOSPFHello []byte) {
	linkTypeEthernet := layers.LinkTypeEthernet
	packet := gopacket.NewPacket(testOSPFHello, linkTypeEthernet, gopacket.Default)
	fmt.Println(packet)
	fmt.Println(packet.Layer(layers.LayerTypeEthernet))
	fmt.Println(packet.Layer(layers.LayerTypeIPv4))
}

